-- ใช้ตัดสินใจสีของ avatar (ชาย/หญิง) แบบมีข้อมูลจริง ไม่ใช่เดาจาก regex บนชื่อ
alter table students
  add column title text not null default 'ไม่ระบุ'
    check (title in ('ไม่ระบุ', 'เด็กชาย', 'เด็กหญิง', 'นาย', 'นาง', 'นางสาว'));
